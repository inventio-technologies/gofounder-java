package com.gofounder.model;

public class User_Link {
	
	private String usershareemail;
	private String email;
	private String invitelink;
	private String date;
	private String link;
	
	
	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getInvitelink() {
		return invitelink;
	}

	public void setInvitelink(String invitelink) {
		this.invitelink = invitelink;
	}

	public String getUsershareemail() {
		return usershareemail;
	}

	public void setUsershareemail(String usershareemail) {
		this.usershareemail = usershareemail;
	}
	

}
